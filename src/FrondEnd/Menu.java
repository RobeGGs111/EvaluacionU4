    private void btnAbrirCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirCActionPerformed
    
        if(caja == 0){
            if (cbxCajas.getSelectedIndex() != 0) {
                if (!"".equalsIgnoreCase(txtSaldoI.getText())) {
                    if (Integer.parseInt(txtSaldoI.getText()) >= 500) {
                        JOptionPane.showMessageDialog(this,"Se abrio caja exitosamente");
                        txtSaldoI.setText("");
                        txtRol.setText("");
                        caja = 1;
                    }else{
                        JOptionPane.showMessageDialog(this,"El saldo es menor a lo esperado: 500");
                    }     
                }else{
                    JOptionPane.showMessageDialog(this,"Inserta el Saldo Inicial");
                }
            }else{
                JOptionPane.showMessageDialog(this,"Escoge tu caja asignada");
            }
        }else{
            JOptionPane.showMessageDialog(this,"Ya se abrio el turno");
        }
    }//GEN-LAST:event_btnAbrirCActionPerformed

