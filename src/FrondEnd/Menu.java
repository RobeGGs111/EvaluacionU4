    private void btnSalirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalirActionPerformed
        
        this.seguro++;
        switch (seguro) {
            case 1 -> JOptionPane.showMessageDialog(null, "¿Estás seguro? ");
            case 2 -> JOptionPane.showMessageDialog(null, "¿Pero bien seguro? ");
            case 3 -> {
                cx.conectar();
                cx.desconectar();
                System.exit(0);
                seguro = 0; 
            }
            default -> {
            }
        }
        
    }//GEN-LAST:event_btnSalirActionPerformed

